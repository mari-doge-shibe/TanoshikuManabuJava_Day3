package academy.learnprogramming;

public class Practice1 {
    // declare a char-type variable and display it in the octal notation
    public static void main(String[] args) {
        char c1 = 'a';
        String s;
        s = Integer.toOctalString(c1);
        System.out.println("Character code of '" + c1 + "' is " + s);

    }





}
